package G1_Iniciante;

import java.text.DecimalFormat;
import java.util.Scanner;

/*
Prolema
	Neste problema, deve-se ler o código de uma peça 1, o número de peças 1, o valor unitário de 
	cada peça 1, o código de uma peça 2, o número de peças 2 e o valor unitário de cada peça 2. 
	Após, calcule e mostre o valor a ser pago.

Entrada
	O arquivo de entrada contém duas linhas de dados. Em cada linha haverá 3 valores, respectivamente 
	dois inteiros e um valor com 2 casas decimais.

Saída
	A saída deverá ser uma mensagem conforme o exemplo fornecido abaixo, lembrando de deixar um espaço 
	após os dois pontos e um espaço após o $. O valor deverá ser apresentado com 2 casas após o ponto.  */

public class P1010_CalculoSimples {
	public static void main(String[] args) {
		Scanner s = new Scanner(System.in);
		
		int cod = s.nextInt();
		double i = s.nextDouble()*s.nextDouble();
		cod = s.nextInt();
		i += s.nextDouble()*s.nextDouble();
		DecimalFormat d = new DecimalFormat("0.00");
		System.out.println("VALOR A PAGAR: R$ " + d.format(i));
		s.close();
	}

}

